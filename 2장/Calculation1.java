/*정수형으로 만든 사칙연산*/

public class Calculation1 {
    public static void main(String[] args) {
        int a=10;
        int b=5;

        System.out.println(a+b); /*덧셈*/
        System.out.println(a-b); /*뺄셈*/
        System.out.println(a*b); /*곱셈*/
        System.out.println(a/b); /*나눗셈*/
        System.out.println(a%b); /*나머지*/
    }
}
